package com.nalashaa.qrdamu2.model;

public class EntryRelationshipPaymentSource {

	private String payerCode;
	
	private String payerDisplayName;
	
	private String payerValue;
	
	private String payerLow;
	
	private String payerHigh;

	public String getPayerCode() {
		return payerCode;
	}

	public void setPayerCode(String payerCode) {
		this.payerCode = payerCode;
	}

	public String getPayerDisplayName() {
		return payerDisplayName;
	}

	public void setPayerDisplayName(String payerDisplayName) {
		this.payerDisplayName = payerDisplayName;
	}

	public String getPayerValue() {
		return payerValue;
	}

	public void setPayerValue(String payerValue) {
		this.payerValue = payerValue;
	}

	public String getPayerLow() {
		return payerLow;
	}

	public void setPayerLow(String payerLow) {
		this.payerLow = payerLow;
	}

	public String getPayerHigh() {
		return payerHigh;
	}

	public void setPayerHigh(String payerHigh) {
		this.payerHigh = payerHigh;
	}
}
